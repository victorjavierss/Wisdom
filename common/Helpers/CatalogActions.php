<?php
class helpers_CatalogActions extends Wisdom_Helper{
	public function catalogActions($value){	
		$img_del  = $this->img('del', 'Borrar');
		$img_del  = "<a href='#' class='boton' onclick='catalog.delete($value)'>{$img_del}</a>";
		$img_view = $this->img('view', 'Ver');
	  	return $img_del . $img_view;
	}
}


?>